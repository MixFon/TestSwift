// Приведение типом. Из одного типа данных получаем доугой
let one: Double = 1.5
let two: Int = 2

// Оператор сложения
let sumOneAndTwo = Int(one) + two
print(sumOneAndTwo)

// Оператор вычитания
let diff = two - Int(one)
print(diff)

// Оператор произведения
let multi = two * Int(one)
print(multi)

// Оператор деления
let div = two / Int(one)
print(div)
